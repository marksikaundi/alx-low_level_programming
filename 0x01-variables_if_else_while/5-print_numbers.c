#include <stdio.h>

/**
 * main - entry point
 *
 * Description: using the main function
 *
 * this program prints "all single digit numbers of base 10 starting from 0
 *
 * Return: always returns 0
 */

int main(void)

{

	int i;

	for (i = 0; i < 10; i++)

	{

		printf("%i", i);

	}

	printf("\n");

	return (0);

}
